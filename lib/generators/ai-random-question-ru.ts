function randomIndex(max: number): number {
  return Math.floor(Math.random() * max);
}

function pick<T>(pool: readonly T[]): T {
  return pool[randomIndex(pool.length)];
}

function resolveCategory(
  category: AiQuestionCategoryRu,
): Exclude<AiQuestionCategoryRu, "random"> {
  if (category === "random") {
    return pick(ALL_CATEGORIES);
  }
  return category;
}

function buildCandidate(options: AiQuestionOptionsRu): string {
  const resolvedCategory = resolveCategory(options.category);
  const bank = QUESTION_BANKS[resolvedCategory];
  return pick(bank);
}

export function validateAiQuestionOptionsRu(
  options: AiQuestionOptionsRu,
): AiQuestionValidationResultRu {
  const { count } = options;

  if (!Number.isSafeInteger(count) || count < MIN_COUNT || count > MAX_COUNT) {
    return {
      valid: false,
      error: `Количество вопросов должно быть целым числом от ${MIN_COUNT} до ${MAX_COUNT}.`,
    };
  }

  return { valid: true };
}

export function generateAiQuestionsRu(
  options: AiQuestionOptionsRu,
): AiQuestionGenerationResultRu {
  const validation = validateAiQuestionOptionsRu(options);

  if (!validation.valid) {
    throw new Error(validation.error);
  }

  const { count } = options;
  const results = new Set<string>();
  const maxAttempts = count * MAX_ATTEMPTS_MULTIPLIER;
  let attempts = 0;

  while (results.size < count && attempts < maxAttempts) {
    const candidate = buildCandidate(options);
    if (candidate.length > 0) {
      results.add(candidate);
    }
    attempts += 1;
  }

  let fallbackSuffix = 1;
  const maxFallbackAttempts = count * MAX_ATTEMPTS_MULTIPLIER;
  while (results.size < count && fallbackSuffix <= maxFallbackAttempts) {
    const base = buildCandidate(options);
    const candidate = `${base} (${fallbackSuffix})`;
    if (!results.has(candidate)) {
      results.add(candidate);
    }
    fallbackSuffix += 1;
  }

  return { questions: Array.from(results) };
}

export function formatAiQuestionResultsRu(questions: readonly string[]): string {
  return questions.join("\n");
}
